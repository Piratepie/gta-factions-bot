export const connectToGameCP = async (username: string, password: string) => {
	const authUrl = "https://grandtheftarma.com/gamecp/login/auth";
	const dasboardUrl = "https://grandtheftarma.com/gamecp";
	const auth = await superagent
		.post(authUrl)
		.send({
			username: username,
			password: password,
			otp: "",
			remember: "on",
			redirect: "",
		})
		.set("Content-Type", "application/x-www-form-urlencoded");

	let dashboard = await superagent.get(dasboardUrl);

	return !dashboard.text.includes("Please enter your credentials to login.");
};

